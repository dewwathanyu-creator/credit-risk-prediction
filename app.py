import streamlit as st
import pandas as pd
import joblib

# ── Page config ──────────────────────────────────────────────
st.set_page_config(
    page_title="Credit Risk Predictor",
    layout="wide"
)

# ── Load model & encoders ────────────────────────────────────
model = joblib.load("extra_trees_credit_model.pkl")
encoders = {
    col: joblib.load(f"{col}_encoder.pkl")
    for col in ["Sex", "Housing", "Saving accounts", "Checking account"]
}

def safe_encode(encoder, value):
    return list(encoder.classes_).index(str(value))

# ── Header ───────────────────────────────────────────────────
st.title("Credit Risk Predictor")
st.markdown(
    "Enter applicant information below to predict whether their credit risk is **Good** or **Bad**."
)
st.divider()

# ── Sidebar inputs ───────────────────────────────────────────
with st.sidebar:
    st.header("Applicant Information")
    st.markdown("Fill in the details below:")

    st.subheader("Personal Details")
    age = st.number_input("Age", min_value=18, max_value=80, value=30)
    sex = st.selectbox("Sex", ["male", "female"])
    job = st.selectbox(
        "Job Type",
        options=[0, 1, 2, 3],
        format_func=lambda x: {
            0: "0 — Unskilled (non-resident)",
            1: "1 — Unskilled (resident)",
            2: "2 — Skilled",
            3: "3 — Highly skilled"
        }[x]
    )
    housing = st.selectbox("Housing", ["own", "rent", "free"])

    st.subheader("Financial Details")
    saving_accounts = st.selectbox(
        "Saving Accounts", ["little", "moderate", "quite rich", "rich"]
    )
    checking_account = st.selectbox(
        "Checking Account", ["little", "moderate", "rich"]
    )
    credit_amount = st.number_input(
        "Credit Amount (DM)", min_value=0, value=3000, step=100
    )
    duration = st.number_input(
        "Loan Duration (months)", min_value=1, max_value=72, value=12
    )

    predict_btn = st.button("Predict Credit Risk", use_container_width=True, type="primary")

# ── Main area ────────────────────────────────────────────────
col1, col2 = st.columns([1, 1])

with col1:
    st.subheader("Input Summary")
    summary = {
        "Age": age,
        "Sex": sex,
        "Job Type": job,
        "Housing": housing,
        "Saving Accounts": saving_accounts,
        "Checking Account": checking_account,
        "Credit Amount (DM)": f"{credit_amount:,}",
        "Duration (months)": duration,
    }
    for k, v in summary.items():
        st.markdown(f"**{k}:** {v}")

with col2:
    st.subheader("Prediction Result")

    if predict_btn:
        input_df = pd.DataFrame({
            "Age": [age],
            "Sex": [safe_encode(encoders["Sex"], sex)],
            "Job": [job],
            "Housing": [safe_encode(encoders["Housing"], housing)],
            "Saving accounts": [safe_encode(encoders["Saving accounts"], saving_accounts)],
            "Checking account": [safe_encode(encoders["Checking account"], checking_account)],
            "Credit amount": [credit_amount],
            "Duration": [duration],
        })

        pred = model.predict(input_df)[0]
        proba = model.predict_proba(input_df)[0]

        if pred == 1:
            confidence = proba[1]
            st.success("### ✅ GOOD Credit Risk")
            st.markdown("The model predicts this applicant is **likely to repay** the loan.")
        else:
            confidence = proba[0]
            st.error("### ❌ BAD Credit Risk")
            st.markdown("The model predicts this applicant is **at risk of defaulting**.")

        st.markdown(f"**Model confidence:** {confidence:.1%}")
        st.progress(float(confidence))

        st.caption(
            "ℹ️ This prediction is generated by a machine learning model and should be used "
            "as a decision-support tool, not as the sole basis for credit decisions."
        )
    else:
        st.info("Fill in the applicant details in the sidebar and click **Predict Credit Risk**.")

# ── Footer ───────────────────────────────────────────────────
st.divider()
st.caption("Model: Extra Trees Classifier · Dataset: German Credit Data · Accuracy: 64.8%")
